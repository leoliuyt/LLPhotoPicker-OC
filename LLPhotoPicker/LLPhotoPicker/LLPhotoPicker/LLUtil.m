+ (BOOL)checkUseRecord
{
    __block BOOL bCanRecord = YES;
    if ([[[UIDevice currentDevice] systemVersion] compare:@"7.0"] != NSOrderedAscending)
    {
        AVAudioSession *audioSession = [AVAudioSession sharedInstance];
        if ([audioSession respondsToSelector:@selector(requestRecordPermission:)]) {
            [audioSession performSelector:@selector(requestRecordPermission:) withObject:^(BOOL granted) {
                bCanRecord = granted;
            }];
        }
    }
    return [self checkUseEquipment:@"麦克风" support:YES allow:bCanRecord camera:NO];
}

+ (BOOL)checkUseEquipment:(NSString *)equipment support:(BOOL)isSupport allow:(BOOL)isAllow camera:(BOOL)isCamera
{
//    LLApp *app = [[LLApp alloc] init];
//    UIViewController* vc = app.viewController;
    UIViewController *vc = nil;
    CGFloat fSystemVersion = [[[UIDevice currentDevice] systemVersion] floatValue];
    if ([[[UIDevice currentDevice] systemVersion] floatValue] < 7.0f) {
        UIAlertView *alert = [[UIAlertView alloc] initWithTitle:@"系统版本太低，需要升级"
                                                        message:nil
                                                       delegate:nil
                                              cancelButtonTitle:@"确定"
                                              otherButtonTitles:nil];
        [alert show];
        return NO;
    }
    
    
    if (!isSupport) {
        
        if (fSystemVersion < 8.0f) {
            UIAlertView *alert = [[UIAlertView alloc] initWithTitle:[NSString stringWithFormat:@"你的设备不支持%@",equipment]
                                                            message:nil
                                                           delegate:nil
                                                  cancelButtonTitle:@"确定"
                                                  otherButtonTitles:nil];
            [alert show];
        } else if (fSystemVersion >= 8.0f) {
            UIAlertController *alertController = [UIAlertController alertControllerWithTitle:[NSString stringWithFormat:@"你的设备不支持%@",equipment]
                                                                                     message:nil
                                                                              preferredStyle:UIAlertControllerStyleAlert];
            
            UIAlertAction *cancelAction = [UIAlertAction actionWithTitle:@"确定"
                                                                   style:UIAlertActionStyleCancel
                                                                 handler:^(UIAlertAction *action)
                                           {}];
            [alertController addAction:cancelAction];
            
            [vc presentViewController:alertController animated:YES completion:nil];
        }
        return NO;
    }
    
    if (!isAllow) {
        
        if (!isCamera) {
            [self checkSystemVersion:fSystemVersion equipment:equipment vc:vc];
            
        }else if ([AVCaptureDevice respondsToSelector:@selector(authorizationStatusForMediaType:)]) {
            NSString *mediaType = AVMediaTypeVideo;
            AVAuthorizationStatus authStatus = [AVCaptureDevice authorizationStatusForMediaType:mediaType];
            
            if(authStatus != AVAuthorizationStatusAuthorized) {
                [self checkSystemVersion:fSystemVersion equipment:equipment vc:vc];
                return NO;
            }
        }
        return NO;
    }
    return YES;
}

