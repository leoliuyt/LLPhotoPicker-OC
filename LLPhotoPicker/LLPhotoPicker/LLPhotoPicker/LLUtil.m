#pragma mark 获取视频文件的缩略图
+ (UIImage*) thumbnailImageForVideo:(NSURL *)videoURL atTime:(NSTimeInterval)time {
    AVURLAsset *asset = [[AVURLAsset alloc] initWithURL:videoURL options:nil];
    NSParameterAssert(asset);
    AVAssetImageGenerator *assetImageGenerator = [[AVAssetImageGenerator alloc] initWithAsset:asset];
    assetImageGenerator.appliesPreferredTrackTransform = YES;
    assetImageGenerator.apertureMode = AVAssetImageGeneratorApertureModeEncodedPixels;
    
    CGImageRef thumbnailImageRef = NULL;
    CFTimeInterval thumbnailImageTime = time;
    NSError *thumbnailImageGenerationError = nil;
    thumbnailImageRef = [assetImageGenerator copyCGImageAtTime:CMTimeMake(thumbnailImageTime, 60) actualTime:NULL error:&thumbnailImageGenerationError];
    
    if (!thumbnailImageRef)
        NSLog(@"thumbnailImageGenerationError %@", thumbnailImageGenerationError);
    
    UIImage *thumbnailImage = thumbnailImageRef ? [[UIImage alloc] initWithCGImage:thumbnailImageRef] : nil;
    
    return thumbnailImage;
}

//时间处理
+ (NSString*)commonStringForSecond:(NSInteger)aSecond
{
    NSString *commonString = @"00:00";
    if (aSecond) {
        NSInteger hour = aSecond/3600;
        NSInteger minute = (aSecond%3600)/60;
        NSInteger second = (aSecond%3600)%60;
        
        if (hour > 0) {
            commonString = [NSString stringWithFormat:@"%02tu:%02tu:%02tu", hour, minute, second];
        } else {
            commonString = [NSString stringWithFormat:@"%02tu:%02tu", minute, second];
        }
    }
    return commonString;
}

//文件夹不存在则创建
+(NSString *)createDirectoriesIfNeededAtPath:(NSString *)aPath{
    NSURL *url;
    [url URLByDeletingLastPathComponent];
    NSString *path = [aPath stringByDeletingLastPathComponent];
    if (![[NSFileManager defaultManager] fileExistsAtPath:path]) {
        [[NSFileManager defaultManager] createDirectoryAtPath:path withIntermediateDirectories:YES attributes:nil error:nil];
    }
    return aPath;
}

+(NSURL *)createDirectoriesIfNeededAtURL:(NSURL *)aURL{
    NSURL *url = [aURL URLByDeletingLastPathComponent];
    if (![[NSFileManager defaultManager] fileExistsAtPath:url.path]) {
        [[NSFileManager defaultManager] createDirectoryAtURL:url withIntermediateDirectories:YES attributes:nil error:nil];
    }
    return aURL;
}

